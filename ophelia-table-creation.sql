CREATE TABLE openweathermap_forecast (
  dt BIGINT,
  temp DECIMAL(7,3) NULL DEFAULT NULL,
  feels_like DECIMAL(7,3) NULL DEFAULT NULL,
  pressure DECIMAL(7,3) NULL DEFAULT NULL,
  humidity DECIMAL(7,3) NULL DEFAULT NULL,
  dew_point DECIMAL(7,3) NULL DEFAULT NULL,
  uvi DECIMAL(7,3) NULL DEFAULT NULL,
  clouds DECIMAL(7,3) NULL DEFAULT NULL,
  visibility DECIMAL(7,3) NULL DEFAULT NULL,
  wind_speed DECIMAL(7,3) NULL DEFAULT NULL,
  wind_deg DECIMAL(7,3) NULL DEFAULT NULL,
  wind_gust DECIMAL(7,3) NULL DEFAULT NULL,
  weather_id INT NULL DEFAULT NULL,
  weather_name VARCHAR(255) NULL DEFAULT NULL,
  weather_description VARCHAR(255) NULL DEFAULT NULL,
  weather_icon VARCHAR(255) NULL DEFAULT NULL,
  pop DECIMAL(7,3) NULL DEFAULT NULL,
  rain DECIMAL(7,3) NULL DEFAULT NULL,
  KEY(`dt`),
  INDEX(`dt`)
);
